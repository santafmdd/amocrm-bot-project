def rows_to_sheet_matrix_v3(*, rows: list[dict[str, Any]], columns: list[str]) -> list[list[Any]]:
    matrix: list[list[Any]] = []
    for row in rows:
        normalized = _normalized_row_lookup(row)
        values: list[Any] = []
        for col in columns:
            key = str(col or "").strip()
            value = row.get(key)
            if value is None:
                alias_target = _HEADER_ALIASES.get(_norm(key), "")
                if alias_target:
                    value = row.get(alias_target)
            if value is None:
                value = normalized.get(_norm(key), "")
            if value is None:
                value = ""
            values.append(value)
        matrix.append(values)
    return matrix


def _normalized_row_lookup(row: dict[str, Any]) -> dict[str, Any]:
    out: dict[str, Any] = {}
    for key, value in row.items():
        nk = _norm(str(key or ""))
        if nk and nk not in out:
            out[nk] = value
    return out


def _norm(value: str) -> str:
    return " ".join(str(value or "").strip().lower().split())

